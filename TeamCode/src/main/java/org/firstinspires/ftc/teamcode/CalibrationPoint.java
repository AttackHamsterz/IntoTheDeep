package org.firstinspires.ftc.teamcode;

public class CalibrationPoint
{
    public double x;
    public double y;
    public double ticks;
    public double legs;

    public CalibrationPoint(double x, double y, double ticks, double legs) {
        this.x = x;
        this.y = y;
        this.ticks = ticks;
        this.legs = legs;
    }
}
